    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // "Go" button pressed

        updateName(); // Get the name that was entered
        
        if (nameIsValid()) // Check that the name is a valid name
        {
            this.lblNameRequired.setVisible(false);
            openQuestionGUI(); // Open the Question screen
        }
        else
        {
            this.lblNameRequired.setVisible(true); // Needs a name
        }
    }//GEN-LAST:event_btnStartActionPerformed

