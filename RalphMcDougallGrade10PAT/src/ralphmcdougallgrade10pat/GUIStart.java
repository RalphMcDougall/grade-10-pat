    private void updateName()
    {
        // Get the new name
        this.NAME = txtNameSurname.getText();
    }
    
    private void openQuestionGUI()
    {
        // Open the Question screen
        GUIQuestion GUIQ = new GUIQuestion(this.SELECTED_TARGET, this, this.SELECTED_OPERATOR, this.SELECTED_DIFFICULTY, this.NAME);
        GUIQ.setVisible(true); // Make it visible
        this.setVisible(false); // Make the main screen invisible
    }
    
    private void openOptionsGUI()
    {
        // Open the Options screen
        GUIOptions GUIO = new GUIOptions(this, this.SELECTED_OPERATOR, this.SELECTED_DIFFICULTY, this.SELECTED_TARGET);
        GUIO.setVisible(true); // Make it visible
        this.setVisible(false); // Make the main screen invisible
    }
    
    private boolean nameIsValid()
    {
        // Check that the name is valid
        for (int i = 0; i < this.NAME.length(); ++i)
        {
            if (this.NAME.charAt(i) != ' ')
            {
                // There is a character present
                return true;
            }
        }
        return false; // No character found
    }
    
    private void openRecordsGUI()
    {
        // Open the Records screen
        GUIRecords GUIR = new GUIRecords(this);
        GUIR.setVisible(true); // Make it visible
        this.setVisible(false); // Make the main screen invisible
    }
    
